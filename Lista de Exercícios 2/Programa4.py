numero = int(input("Digite qualquer número: "))

if numero % 2 != 0:
    print ("Este número é ímpar.")
else:
    print ("Este número não é ímpar.")

if numero % 3 != 0:
    print ("Este número não é múltiplo de 3.")
else:
    print ("Este número é múltiplo de 3.")

if numero % 102 != 0:
    print ("Este número não é múltiplo de 102.")
else:
    print ("Este número é múltiplo de 102.")